DECLARE
  v_attorney RECORD;
  v_day INTEGER;
  v_times TIME[] := ARRAY[
    '09:00'::TIME, '09:30'::TIME, '10:00'::TIME, '10:30'::TIME,
    '11:00'::TIME, '11:30'::TIME,
    '14:00'::TIME, '14:30'::TIME, '15:00'::TIME, '15:30'::TIME,
    '16:00'::TIME, '16:30'::TIME
  ];
  v_ends TIME[] := ARRAY[
    '09:30'::TIME, '10:00'::TIME, '10:30'::TIME, '11:00'::TIME,
    '11:30'::TIME, '12:00'::TIME,
    '14:30'::TIME, '15:00'::TIME, '15:30'::TIME, '16:00'::TIME,
    '16:30'::TIME, '17:00'::TIME
  ];
  v_idx INTEGER;
BEGIN
  FOR v_attorney IN SELECT id FROM attorneys LOOP
    FOR v_day IN 1..5 LOOP  -- 월(1) ~ 금(5)
      FOR v_idx IN 1..12 LOOP
        INSERT INTO available_slots (attorney_id, day_of_week, start_time, end_time)
        VALUES (v_attorney.id, v_day, v_times[v_idx], v_ends[v_idx]);
      END LOOP;
    END LOOP;
  END LOOP;
END $$;

