-- ============================================================
-- 초기 관리자 계정 생성 안내 (seed_admin.sql)
-- ============================================================
--
-- 이 파일은 "실행용 SQL"이 아니라 Supabase Auth 관리자 계정을
-- 생성하는 두 가지 방법을 안내하는 문서입니다.
--
-- Supabase의 auth.users 테이블은 내부 스키마(auth schema)에 속하며,
-- 일반 SQL INSERT로 직접 사용자를 생성하면 비밀번호 해싱·메타데이터
-- 초기화가 누락되어 로그인이 불가능합니다.
-- 따라서 아래 두 방법 중 하나를 사용하세요.
--
-- ------------------------------------------------------------
-- 방법 1. Supabase Dashboard에서 수동 생성 (권장: 단건/최초 1회)
-- ------------------------------------------------------------
--
-- 1) https://supabase.com/dashboard 로그인
-- 2) 해당 프로젝트 선택
-- 3) 좌측 메뉴 → Authentication → Users
-- 4) 우측 상단 "Add user" → "Create new user" 클릭
-- 5) 입력:
--      Email:    admin@lawfirm.co.kr
--      Password: (10자 이상, 영문+숫자+특수문자)
--      Auto Confirm User: ✅ 체크 (이메일 확인 즉시 완료 처리)
-- 6) "Create user" 클릭
--
-- ------------------------------------------------------------
-- 방법 2. Node.js 스크립트 (권장: 재현 가능한 환경 셋업)
-- ------------------------------------------------------------
--
-- 사용처: 개발 초기 세팅, CI/CD 자동화, 신규 환경 구축
--
-- 1) .env.local 에 아래 변수를 채웁니다:
--      SUPABASE_SERVICE_ROLE_KEY=<Dashboard → Settings → API → service_role>
--      INITIAL_ADMIN_PASSWORD=<10자 이상 강력한 비밀번호>
--
-- 2) 프로젝트 루트에서 실행:
--      npm run create-admin
--
-- 3) 성공 메시지 확인:
--      ✅ 관리자 계정 생성 완료: admin@lawfirm.co.kr (id=<uuid>)
--
-- 4) Supabase Dashboard → Authentication → Users 에서
--    해당 사용자가 "Email Confirmed" 상태인지 확인.
--
-- ------------------------------------------------------------
-- 보안 주의사항
-- ------------------------------------------------------------
--
-- * SUPABASE_SERVICE_ROLE_KEY 는 RLS 를 우회하는 최상위 권한입니다.
--   반드시 서버 전용 환경변수로만 사용하고,
--   클라이언트 번들(NEXT_PUBLIC_ 접두사)에 절대 노출하지 마세요.
--
-- * INITIAL_ADMIN_PASSWORD 는 생성 직후 Supabase Dashboard 또는
--   관리자 페이지의 비밀번호 변경 기능으로 재설정할 것을 권장합니다.
--
-- * 프로덕션 환경에서는 계정 생성 후 .env 에서 INITIAL_ADMIN_PASSWORD
--   값을 제거하세요.
